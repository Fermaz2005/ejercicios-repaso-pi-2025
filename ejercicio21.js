//Menu de opciones
let opcion = 'editar'
switch(opcion){
    case "ver":
    console.log("Mostrando perfil");
    break;
    case "editar":
    console.log("Editando perfil");
    break;
    case "salir":
    console.log("Saliendo del perfil");
    break
    default:
    console.log("Opcion no valida");
}